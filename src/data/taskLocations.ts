export const mockTaskLocations: TaskLocation[] = taskLocationProperties.flatMap((property) =>
  taskLocationSeed[property.id].map(([id, name, roomNumber]) => ({
    id,
    propertyId: property.id,
    property: property.name,
    name,
    ...(roomNumber ? { roomNumber } : {}),
    isDeleted: false,
    isPassive: false,
  }))
);

export const activeTaskLocations = mockTaskLocations.filter((location) => (
  !location.isDeleted && !location.isPassive
));

export const taskLocationGroups = taskLocationProperties.map((property) => ({
  property,
  options: activeTaskLocations
    .filter((location) => location.propertyId === property.id)
    .map((location) => ({
      label: location.name,
      value: `${location.propertyId}:${location.id}`,
      location,
    })),
})).filter((group) => group.options.length > 0);

export const taskLocationOptions = taskLocationGroups.flatMap((group) =>
  group.options.map((option) => ({
    label: `${group.property.name} - ${option.label}`,
    value: option.value,
  }))
);
